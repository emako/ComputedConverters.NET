public class ToolTipSetStateToSourceAction : TriggerAction<ToolTip>
{
    public ToolTipSetStateToSourceAction()
    {
    }

    public ToolTipUnbindableCanReadProperty Property
    {
        get => (ToolTipUnbindableCanReadProperty)GetValue(PropertyProperty);
        set => SetValue(PropertyProperty, value);
    }

    public static readonly DependencyProperty PropertyProperty =
        DependencyProperty.Register(nameof(Property), typeof(ToolTipUnbindableCanReadProperty), typeof(ToolTipSetStateToSourceAction), new PropertyMetadata());

    [Bindable(BindableSupport.Default, BindingDirection.TwoWay)]
    public object Source
    {
        get => GetValue(SourceProperty);
        set => SetValue(SourceProperty, value);
    }

    public static readonly DependencyProperty SourceProperty =
        DependencyProperty.Register(nameof(Source), typeof(object), typeof(ToolTipSetStateToSourceAction), new PropertyMetadata(null));

    protected override void Invoke(object parameter)
    {
        switch (Property)
        {
            case ToolTipUnbindableCanReadProperty.HasContent:
                if ((bool)Source != AssociatedObject.HasContent)
                {
                    Source = AssociatedObject.HasContent;
                }
                break;

            case ToolTipUnbindableCanReadProperty.ActualWidth:
                if ((double)Source != AssociatedObject.ActualWidth)
                {
                    Source = AssociatedObject.ActualWidth;
                }
                break;

            case ToolTipUnbindableCanReadProperty.ActualHeight:
                if ((double)Source != AssociatedObject.ActualHeight)
                {
