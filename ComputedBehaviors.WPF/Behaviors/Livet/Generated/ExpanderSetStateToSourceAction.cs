public class ExpanderSetStateToSourceAction : TriggerAction<Expander>
{
    public ExpanderSetStateToSourceAction()
    {
    }

    public ExpanderUnbindableCanReadProperty Property
    {
        get => (ExpanderUnbindableCanReadProperty)GetValue(PropertyProperty);
        set => SetValue(PropertyProperty, value);
    }

    public static readonly DependencyProperty PropertyProperty =
        DependencyProperty.Register(nameof(Property), typeof(ExpanderUnbindableCanReadProperty), typeof(ExpanderSetStateToSourceAction), new PropertyMetadata());

    [Bindable(BindableSupport.Default, BindingDirection.TwoWay)]
    public object Source
    {
        get => GetValue(SourceProperty);
        set => SetValue(SourceProperty, value);
    }

    public static readonly DependencyProperty SourceProperty =
        DependencyProperty.Register(nameof(Source), typeof(object), typeof(ExpanderSetStateToSourceAction), new PropertyMetadata(null));

    protected override void Invoke(object parameter)
    {
        switch (Property)
        {
            case ExpanderUnbindableCanReadProperty.HasHeader:
                if ((bool)Source != AssociatedObject.HasHeader)
                {
                    Source = AssociatedObject.HasHeader;
                }
                break;

            case ExpanderUnbindableCanReadProperty.HasContent:
                if ((bool)Source != AssociatedObject.HasContent)
                {
                    Source = AssociatedObject.HasContent;
                }
                break;

            case ExpanderUnbindableCanReadProperty.ActualWidth:
                if ((double)Source != AssociatedObject.ActualWidth)
                {
                    Source = AssociatedObject.ActualWidth;
                }
                break;

