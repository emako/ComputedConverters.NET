    AreAnyTouchesOver,
    AreAnyTouchesDirectlyOver,
    AreAnyTouchesCapturedWithin,
    AreAnyTouchesCaptured,
    IsSealed
}

public class FrameSetStateToSourceAction : TriggerAction<Frame>
{
    public FrameSetStateToSourceAction()
    {
    }

    public FrameUnbindableCanReadProperty Property
    {
        get => (FrameUnbindableCanReadProperty)GetValue(PropertyProperty);
        set => SetValue(PropertyProperty, value);
    }

    public static readonly DependencyProperty PropertyProperty =
        DependencyProperty.Register(nameof(Property), typeof(FrameUnbindableCanReadProperty), typeof(FrameSetStateToSourceAction), new PropertyMetadata());

    [Bindable(BindableSupport.Default, BindingDirection.TwoWay)]
    public object Source
    {
        get => GetValue(SourceProperty);
        set => SetValue(SourceProperty, value);
    }

    public static readonly DependencyProperty SourceProperty =
        DependencyProperty.Register(nameof(Source), typeof(object), typeof(FrameSetStateToSourceAction), new PropertyMetadata(null));

    protected override void Invoke(object parameter)
    {
        switch (Property)
        {
            case FrameUnbindableCanReadProperty.CanGoForward:
                if ((bool)Source != AssociatedObject.CanGoForward)
                {
                    Source = AssociatedObject.CanGoForward;
                }
                break;

            case FrameUnbindableCanReadProperty.CanGoBack:
                if ((bool)Source != AssociatedObject.CanGoBack)
                {
                    Source = AssociatedObject.CanGoBack;
                }
                break;

            case FrameUnbindableCanReadProperty.CurrentSource:
                if ((System.Uri)Source != AssociatedObject.CurrentSource)
                {
                    Source = AssociatedObject.CurrentSource;
                }
                break;

            case FrameUnbindableCanReadProperty.BackStack:
                if ((System.Collections.IEnumerable)Source != AssociatedObject.BackStack)
                {
                    Source = AssociatedObject.BackStack;
                }
                break;

            case FrameUnbindableCanReadProperty.ForwardStack:
                if ((System.Collections.IEnumerable)Source != AssociatedObject.ForwardStack)
                {
                    Source = AssociatedObject.ForwardStack;
                }
                break;

            case FrameUnbindableCanReadProperty.HasContent:
                if ((bool)Source != AssociatedObject.HasContent)
                {
                    Source = AssociatedObject.HasContent;
                }
                break;

            case FrameUnbindableCanReadProperty.ActualWidth:
                if ((double)Source != AssociatedObject.ActualWidth)
                {
                    Source = AssociatedObject.ActualWidth;
                }
                break;

            case FrameUnbindableCanReadProperty.ActualHeight:
