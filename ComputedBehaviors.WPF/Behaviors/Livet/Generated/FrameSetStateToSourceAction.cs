using System.Windows;
using System.Windows.Controls;

namespace ComputedBehaviors;

public enum FrameUnbindableCanReadProperty
{
    CanGoForward,
    CanGoBack,
    CurrentSource,
    BackStack,
    ForwardStack,
    HasContent,
    ActualWidth,
    ActualHeight,
    IsInitialized,
    IsLoaded,
    HasAnimatedProperties,
    IsMeasureValid,
    IsArrangeValid,
    IsMouseDirectlyOver,
    IsMouseOver,
    IsStylusOver,
    IsKeyboardFocusWithin,
    IsMouseCaptured,
    IsMouseCaptureWithin,
