public class WebBrowserSetStateToSourceAction : TriggerAction<WebBrowser>
{
    public WebBrowserSetStateToSourceAction()
    {
    }

    public WebBrowserUnbindableCanReadProperty Property
    {
        get => (WebBrowserUnbindableCanReadProperty)GetValue(PropertyProperty);
        set => SetValue(PropertyProperty, value);
    }

    public static readonly DependencyProperty PropertyProperty =
        DependencyProperty.Register(nameof(Property), typeof(WebBrowserUnbindableCanReadProperty), typeof(WebBrowserSetStateToSourceAction), new PropertyMetadata());

    [Bindable(BindableSupport.Default, BindingDirection.TwoWay)]
    public object Source
    {
        get => GetValue(SourceProperty);
        set => SetValue(SourceProperty, value);
    }

    public static readonly DependencyProperty SourceProperty =
        DependencyProperty.Register(nameof(Source), typeof(object), typeof(WebBrowserSetStateToSourceAction), new PropertyMetadata(null));

    protected override void Invoke(object parameter)
    {
        switch (Property)
        {
            case WebBrowserUnbindableCanReadProperty.Source:
                if ((System.Uri)Source != AssociatedObject.Source)
                {
                    Source = AssociatedObject.Source;
                }
                break;

            case WebBrowserUnbindableCanReadProperty.CanGoBack:
                if ((bool)Source != AssociatedObject.CanGoBack)
                {
                    Source = AssociatedObject.CanGoBack;
                }
                break;

            case WebBrowserUnbindableCanReadProperty.CanGoForward:
                if ((bool)Source != AssociatedObject.CanGoForward)
                {
                    Source = AssociatedObject.CanGoForward;
                }
                break;

