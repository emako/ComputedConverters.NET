public enum WebBrowserUnbindableCanReadProperty
{
    Source,
    CanGoBack,
    CanGoForward,
    ObjectForScripting,
    Document,
    Handle,
    ActualWidth,
    ActualHeight,
    IsInitialized,
    IsLoaded,
