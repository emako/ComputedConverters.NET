                {
                    Source = AssociatedObject.Document;
                }
                break;

            case WebBrowserUnbindableCanReadProperty.Handle:
                if ((nint)Source != AssociatedObject.Handle)
                {
                    Source = AssociatedObject.Handle;
                }
                break;

            case WebBrowserUnbindableCanReadProperty.ActualWidth:
