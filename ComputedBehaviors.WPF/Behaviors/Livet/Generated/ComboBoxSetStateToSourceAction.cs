namespace ComputedBehaviors;

public enum ComboBoxUnbindableCanReadProperty
{
    SelectionBoxItem,
    SelectionBoxItemStringFormat,
    IsSelectionBoxHighlighted,
    HasItems,
    IsGrouping,
    ActualWidth,
    ActualHeight,
    IsInitialized,
    IsLoaded,
    HasAnimatedProperties,
    IsMeasureValid,
    IsArrangeValid,
    IsMouseDirectlyOver,
    IsMouseOver,
    IsStylusOver,
    IsKeyboardFocusWithin,
