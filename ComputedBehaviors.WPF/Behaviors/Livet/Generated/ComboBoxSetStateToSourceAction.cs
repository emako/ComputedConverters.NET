public class ComboBoxSetStateToSourceAction : TriggerAction<ComboBox>
{
    public ComboBoxSetStateToSourceAction()
    {
    }

    public ComboBoxUnbindableCanReadProperty Property
    {
        get => (ComboBoxUnbindableCanReadProperty)GetValue(PropertyProperty);
        set => SetValue(PropertyProperty, value);
    }

    public static readonly DependencyProperty PropertyProperty =
        DependencyProperty.Register(nameof(Property), typeof(ComboBoxUnbindableCanReadProperty), typeof(ComboBoxSetStateToSourceAction), new PropertyMetadata());

    [Bindable(BindableSupport.Default, BindingDirection.TwoWay)]
    public object Source
    {
        get => GetValue(SourceProperty);
        set => SetValue(SourceProperty, value);
    }

    public static readonly DependencyProperty SourceProperty =
        DependencyProperty.Register(nameof(Source), typeof(object), typeof(ComboBoxSetStateToSourceAction), new PropertyMetadata(null));

    protected override void Invoke(object parameter)
    {
        switch (Property)
        {
            case ComboBoxUnbindableCanReadProperty.SelectionBoxItem:
                if (Source != AssociatedObject.SelectionBoxItem)
                {
                    Source = AssociatedObject.SelectionBoxItem;
                }
                break;

            case ComboBoxUnbindableCanReadProperty.SelectionBoxItemStringFormat:
                if ((string)Source != AssociatedObject.SelectionBoxItemStringFormat)
                {
                    Source = AssociatedObject.SelectionBoxItemStringFormat;
                }
                break;

            case ComboBoxUnbindableCanReadProperty.IsSelectionBoxHighlighted:
                if ((bool)Source != AssociatedObject.IsSelectionBoxHighlighted)
                {
