    {
        switch (Property)
        {
            case ListBoxItemUnbindableCanReadProperty.HasContent:
                if ((bool)Source != AssociatedObject.HasContent)
                {
                    Source = AssociatedObject.HasContent;
                }
                break;

            case ListBoxItemUnbindableCanReadProperty.ActualWidth:
                if ((double)Source != AssociatedObject.ActualWidth)
                {
