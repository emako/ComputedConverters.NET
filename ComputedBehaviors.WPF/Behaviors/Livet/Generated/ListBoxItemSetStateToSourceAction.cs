public class ListBoxItemSetStateToSourceAction : TriggerAction<ListBoxItem>
{
    public ListBoxItemSetStateToSourceAction()
    {
    }

    public ListBoxItemUnbindableCanReadProperty Property
    {
        get => (ListBoxItemUnbindableCanReadProperty)GetValue(PropertyProperty);
        set => SetValue(PropertyProperty, value);
    }

    public static readonly DependencyProperty PropertyProperty =
        DependencyProperty.Register(nameof(Property), typeof(ListBoxItemUnbindableCanReadProperty), typeof(ListBoxItemSetStateToSourceAction), new PropertyMetadata());

    [Bindable(BindableSupport.Default, BindingDirection.TwoWay)]
    public object Source
    {
        get => GetValue(SourceProperty);
        set => SetValue(SourceProperty, value);
    }

    public static readonly DependencyProperty SourceProperty =
        DependencyProperty.Register(nameof(Source), typeof(object), typeof(ListBoxItemSetStateToSourceAction), new PropertyMetadata(null));

