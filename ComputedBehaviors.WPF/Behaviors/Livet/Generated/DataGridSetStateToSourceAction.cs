public class DataGridSetStateToSourceAction : TriggerAction<DataGrid>
{
    public DataGridSetStateToSourceAction()
    {
    }

    public DataGridUnbindableCanReadProperty Property
    {
        get => (DataGridUnbindableCanReadProperty)GetValue(PropertyProperty);
        set => SetValue(PropertyProperty, value);
    }

