    AreAnyTouchesDirectlyOver,
    AreAnyTouchesCapturedWithin,
    AreAnyTouchesCaptured,
    IsSealed
}

public class TreeViewSetStateToSourceAction : TriggerAction<TreeView>
{
    public TreeViewSetStateToSourceAction()
    {
    }

    public TreeViewUnbindableCanReadProperty Property
    {
        get => (TreeViewUnbindableCanReadProperty)GetValue(PropertyProperty);
        set => SetValue(PropertyProperty, value);
    }

    public static readonly DependencyProperty PropertyProperty =
        DependencyProperty.Register(nameof(Property), typeof(TreeViewUnbindableCanReadProperty), typeof(TreeViewSetStateToSourceAction), new PropertyMetadata());

    [Bindable(BindableSupport.Default, BindingDirection.TwoWay)]
    public object Source
    {
        get => GetValue(SourceProperty);
        set => SetValue(SourceProperty, value);
    }

    public static readonly DependencyProperty SourceProperty =
        DependencyProperty.Register(nameof(Source), typeof(object), typeof(TreeViewSetStateToSourceAction), new PropertyMetadata(null));

    protected override void Invoke(object parameter)
    {
        switch (Property)
        {
            case TreeViewUnbindableCanReadProperty.SelectedItem:
                if (Source != AssociatedObject.SelectedItem)
                {
                    Source = AssociatedObject.SelectedItem;
                }
                break;

            case TreeViewUnbindableCanReadProperty.SelectedValue:
                if (Source != AssociatedObject.SelectedValue)
                {
                    Source = AssociatedObject.SelectedValue;
                }
                break;

            case TreeViewUnbindableCanReadProperty.HasItems:
                if ((bool)Source != AssociatedObject.HasItems)
                {
                    Source = AssociatedObject.HasItems;
                }
                break;

            case TreeViewUnbindableCanReadProperty.IsGrouping:
                if ((bool)Source != AssociatedObject.IsGrouping)
                {
                    Source = AssociatedObject.IsGrouping;
                }
                break;

            case TreeViewUnbindableCanReadProperty.ActualWidth:
                if ((double)Source != AssociatedObject.ActualWidth)
                {
                    Source = AssociatedObject.ActualWidth;
                }
                break;

            case TreeViewUnbindableCanReadProperty.ActualHeight:
