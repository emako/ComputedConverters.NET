namespace ComputedBehaviors;

public enum TreeViewUnbindableCanReadProperty
{
    SelectedItem,
    SelectedValue,
    HasItems,
    IsGrouping,
    ActualWidth,
    ActualHeight,
    IsInitialized,
    IsLoaded,
    HasAnimatedProperties,
    IsMeasureValid,
    IsArrangeValid,
    IsMouseDirectlyOver,
    IsMouseOver,
    IsStylusOver,
    IsKeyboardFocusWithin,
    IsMouseCaptured,
