public class PageContentSetStateToSourceAction : TriggerAction<PageContent>
{
    public PageContentSetStateToSourceAction()
    {
    }

    public PageContentUnbindableCanReadProperty Property
    {
        get => (PageContentUnbindableCanReadProperty)GetValue(PropertyProperty);
        set => SetValue(PropertyProperty, value);
    }

    public static readonly DependencyProperty PropertyProperty =
        DependencyProperty.Register(nameof(Property), typeof(PageContentUnbindableCanReadProperty), typeof(PageContentSetStateToSourceAction), new PropertyMetadata());

    [Bindable(BindableSupport.Default, BindingDirection.TwoWay)]
    public object Source
    {
        get => GetValue(SourceProperty);
        set => SetValue(SourceProperty, value);
    }

    public static readonly DependencyProperty SourceProperty =
        DependencyProperty.Register(nameof(Source), typeof(object), typeof(PageContentSetStateToSourceAction), new PropertyMetadata(null));

