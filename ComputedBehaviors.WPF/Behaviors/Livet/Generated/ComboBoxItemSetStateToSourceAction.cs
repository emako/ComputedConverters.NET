        DependencyProperty.Register(nameof(Property), typeof(ComboBoxItemUnbindableCanReadProperty), typeof(ComboBoxItemSetStateToSourceAction), new PropertyMetadata());

    [Bindable(BindableSupport.Default, BindingDirection.TwoWay)]
    public object Source
    {
        get => GetValue(SourceProperty);
        set => SetValue(SourceProperty, value);
    }

    public static readonly DependencyProperty SourceProperty =
        DependencyProperty.Register(nameof(Source), typeof(object), typeof(ComboBoxItemSetStateToSourceAction), new PropertyMetadata(null));

