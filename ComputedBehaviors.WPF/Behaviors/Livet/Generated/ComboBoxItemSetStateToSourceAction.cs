public class ComboBoxItemSetStateToSourceAction : TriggerAction<ComboBoxItem>
{
    public ComboBoxItemSetStateToSourceAction()
    {
    }

    public ComboBoxItemUnbindableCanReadProperty Property
    {
        get => (ComboBoxItemUnbindableCanReadProperty)GetValue(PropertyProperty);
        set => SetValue(PropertyProperty, value);
    }

